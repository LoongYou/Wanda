package visiotool  ;

import com4j.*;

/**
 * <p>
 * Position property values of command bars.
 * </p>
 */
public enum tagVisUIBarPosition {
  /**
   * <p>
   * Bar is docked at the left.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visBarLeft, // 0
  /**
   * <p>
   * Bar is docked at the top.
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visBarTop, // 1
  /**
   * <p>
   * Bar is docked at the right.
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visBarRight, // 2
  /**
   * <p>
   * Bar is docked at the bottom.
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visBarBottom, // 3
  /**
   * <p>
   * Bar is floating.
   * </p>
   * <p>
   * The value of this constant is 4
   * </p>
   */
  visBarFloating, // 4
  /**
   * <p>
   * Bar is popup.
   * </p>
   * <p>
   * The value of this constant is 5
   * </p>
   */
  visBarPopup, // 5
  /**
   * <p>
   * Bar is menu.
   * </p>
   * <p>
   * The value of this constant is 6
   * </p>
   */
  visBarMenu, // 6
}
