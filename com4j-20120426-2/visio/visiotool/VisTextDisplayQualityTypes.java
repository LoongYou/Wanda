package visiotool  ;

import com4j.*;

/**
 * <p>
 * Text Display Quality Types
 * </p>
 */
public enum VisTextDisplayQualityTypes {
  /**
   * <p>
   * Obsolete as of Visio 2010, Faster text display (aliased)
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visTextDisplayFaster, // 0
  /**
   * <p>
   * Obsolete as of Visio 2010,  Higher quality text display (anti-aliased)
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visTextDisplayHigherQuality, // 1
  /**
   * <p>
   * Obsolete as of Visio 2010, Clear text display (anti-aliased LCD Display)
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visTextDisplayClear, // 2
}
