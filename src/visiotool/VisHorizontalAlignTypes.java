package visiotool  ;

import com4j.*;

/**
 * <p>
 * Horizontal Alignment types.
 * </p>
 */
public enum VisHorizontalAlignTypes {
  /**
   * <p>
   * No horizontal alignment.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visHorzAlignNone, // 0
  /**
   * <p>
   * Align to left of primary selected shape.
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visHorzAlignLeft, // 1
  /**
   * <p>
   * Align to center of primary selected shape.
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visHorzAlignCenter, // 2
  /**
   * <p>
   * Align to right of primary selected shape.
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visHorzAlignRight, // 3
}
