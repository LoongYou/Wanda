package visiotool  ;

import com4j.*;

/**
 * <p>
 * Styles of buttons in command bars.
 * </p>
 */
public enum tagVisUIButtonStyle {
  /**
   * <p>
   * Default style.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visButtonAutomatic, // 0
  /**
   * <p>
   * Text Only (Always).
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visButtonIcon, // 1
  /**
   * <p>
   * Text Only (in Menus).
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visButtonCaption, // 2
  /**
   * <p>
   * Image and Text.
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visButtonIconandCaption, // 3
}
