package visiotool  ;

import com4j.*;

/**
 */
public enum VisLayoutVertAlignType {
  /**
   * <p>
   * Do not align vertically
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visLayoutVertAlignNone, // 0
  /**
   * <p>
   * Allow Visio to choose how to vertically align
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visLayoutVertAlignDefault, // 1
  /**
   * <p>
   * Align the top edges of shapes
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visLayoutVertAlignTop, // 2
  /**
   * <p>
   * Align the middles of shapes
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visLayoutVertAlignMiddle, // 3
  /**
   * <p>
   * Align the bottom edges of shapes
   * </p>
   * <p>
   * The value of this constant is 4
   * </p>
   */
  visLayoutVertAlignBottom, // 4
}
