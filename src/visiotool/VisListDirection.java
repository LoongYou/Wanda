package visiotool  ;

import com4j.*;

/**
 */
public enum VisListDirection {
  /**
   * <p>
   * Arrange shapes in the list horizontally from left to right
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visListDirLeftToRight, // 0
  /**
   * <p>
   * Arrange shapes in the list horizontally from right to left
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visListDirRightToLeft, // 1
  /**
   * <p>
   * Arrange shapes in the list vertically from top to bottom
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visListDirTopToBottom, // 2
  /**
   * <p>
   * Arrange shapes in the list verticaly from bottom to top
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visListDirBottomToTop, // 3
}
