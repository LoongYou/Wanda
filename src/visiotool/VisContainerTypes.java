package visiotool  ;

import com4j.*;

/**
 */
public enum VisContainerTypes {
  /**
   * <p>
   * Member shapes of the container shape are  not in any managed arrangement
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visContainerTypeNormal, // 0
  /**
   * <p>
   * Some or all of the member shapes of the container shape are arranged as a list
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visContainerTypeList, // 1
}
