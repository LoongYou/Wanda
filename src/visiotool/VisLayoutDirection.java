package visiotool  ;

import com4j.*;

/**
 */
public enum VisLayoutDirection {
  /**
   * <p>
   * Rotates the diagram 90 degrees clockwise.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visLayoutDirRotateRight, // 0
  /**
   * <p>
   * Rotates the diagram 90 degrees counterclockwise.
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visLayoutDirRotateLeft, // 1
  /**
   * <p>
   * Flips the diagram vertically.
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visLayoutDirFlipVert, // 2
  /**
   * <p>
   * Flips the diagram horizontally.
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visLayoutDirFlipHorz, // 3
}
