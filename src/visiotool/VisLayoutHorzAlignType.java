package visiotool  ;

import com4j.*;

/**
 */
public enum VisLayoutHorzAlignType {
  /**
   * <p>
   * Do not align horizontally
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  visLayoutHorzAlignNone, // 0
  /**
   * <p>
   * Allow Visio to choose how to horizontally align
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  visLayoutHorzAlignDefault, // 1
  /**
   * <p>
   * Align the left edges of shapes
   * </p>
   * <p>
   * The value of this constant is 2
   * </p>
   */
  visLayoutHorzAlignLeft, // 2
  /**
   * <p>
   * Align the centers of shapes
   * </p>
   * <p>
   * The value of this constant is 3
   * </p>
   */
  visLayoutHorzAlignCenter, // 3
  /**
   * <p>
   * Align the right edges of shapes
   * </p>
   * <p>
   * The value of this constant is 4
   * </p>
   */
  visLayoutHorzAlignRight, // 4
}
